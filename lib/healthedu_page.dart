在上述任何一種飲食中，水果和蔬菜在食物中扮演著關鍵角色，主要因為水果蔬菜富含維生素、礦物質和抗氧化劑，能量密度也低，是膳食纖維的來源。這些植物性化合物可以調節不同的過程，例如解毒酶，支持免疫系統，調節膽固醇合成，更是抗氧化劑。多項研究表示，蔬菜攝取量的增加，可以降低老年癡呆的風險和延緩認知能力下降。其他研究分析了食用堅果對認知功能的影響，結果表示老年人的整體認知水平更高。堅果含有單不飽和脂肪酸（MUFA）和多不飽和脂肪酸（PUFA），攝取堅果減少了總膽固醇和低密度脂蛋白膽固醇，降低了患心血管疾病的風險疾病和第2型糖尿病。由於這些因素與認知有關，也可能是堅果改善認知功能的基本原理。

抗氧化劑：減少認知衰退的關鍵？
如上所述，大腦對自由基引起的氧化損傷非常敏感。因此，減少氧化反應和保護粒線體可能是非常重要的關鍵。抗氧化劑可透過不同的機制，破壞自由基的傳播或抑制自由基的形成。抗氧化劑有助於清除過多過氧化的物質，防止形成過氧化物。除人體產生的抗氧化酶外，礦物質、維生素、類胡蘿蔔素、穀胱甘肽和多酚等抗氧化劑是對於細胞和生物體的抗氧化防禦機制至關重要。而人類無法自行合成抗壞血酸（維生素C）和生育酚（維生素E），因此必須透過食物獲得。相反，在人體中可合成並具有抗氧化劑作用的物質，包括穀胱甘肽、硫辛酸酸、尿酸、牛磺酸、褪黑激素、輔酶Q和黑色素，但隨著年紀增長合成能力也會降低。

黃酮類化合物
黃酮類化合物研究最多的為多酚類，其存在於草藥，水果，蔬菜，和藥用植物。主要特性為具有抗氧化劑，抗誘變劑，抗發炎劑和抗癌特性，其抗氧化活性能清除游離態自由基;通過抑制酶和螯合微量元素來抑制自由基的產生；並提升抗氧化劑的防禦能力。許多研究表明，類黃酮對腦部有益，食物來源包括柑橘類水果、葡萄酒、綠茶和可可。它們在大腦中的作用機理就是抗氧化、抗發炎。

此外，它們也會增加大腦的血液流動，可增強認知的能力。另外研究發現，橄欖油可以降低氧化發炎反應，Morato等研究（Molecules 2015）表明橄欖油的某些酚類化合物（油橄欖石和三萜烯酸），具有很強的神經保護作用，因此可以作為神經系統疾病的治療。

其他研究證實綠茶可以預防多巴胺神經變性神經元。綠茶中的多酚成分具有很高的抗氧化能力，可以作為神經保護劑並減少神經病變的發生。許多藥理學研究證實了高血漿濃度薑黃素可有效清除羥基和超氧自由基。研究已經表明薑黃素在神經系統中具有有益的治療作用，例如在阿茲海默症和帕金森氏症中，薑黃素減少阿茲海默症中的氧化損傷，對認知功能障礙有保護作用。因此，似乎薑黃素在神經病變疾病中具有有益的治療作用。

類胡蘿蔔素
類胡蘿蔔素是植物和藻類產生的天然有機化合物。除了抗氧化活性，類胡蘿蔔素還有助於調節細胞週期、增強免疫系統、調節細胞信號通路，生長因子的促進作用。類胡蘿蔔素是高度親脂性分子，它們駐留在細胞內以保護細胞膜免受氧化反應。類胡蘿蔔素對阿茲海默症和帕金森氏症的病理生理學的影響已廣泛研究，其通過抑制氧化反應，減少疾病的發生而延緩疾病的發展澱粉樣肽的產生，並抑制發炎細胞因子。b-胡蘿蔔素（維生素A）具有有效的抗氧化劑活性，尤其是提供抗氧化劑在富含脂質的組織（例如神經元）中具有保護作用。因此，b-胡蘿蔔素可以中和脂質過氧化。據報導，在飲食中攝入b-胡蘿蔔素可降低阿茲海默症和其他相關癡呆的發生率。此外，b-胡蘿蔔素可改善粒線體功能，最近的研究指出粒線體可能是神經病變的治療靶點，因此有學者建議使用b-胡蘿蔔素來預防神經病變。

抗壞血酸（維生素C）
維生素C是另一種眾所周知的抗氧化劑，它涉及許多其他重要功能，例如膠原蛋白的產生、酶輔因子、鐵的吸收和刺激免疫系統。此外，有人提出維生素C可能是一種多巴胺，谷氨酸（glutamatergic acid）、GABA和膽鹼神經元的神經調節劑。維生素C被認為是人體細胞外液中最重要的水溶性抗氧化劑。

研究證據表明，維生素C對神經退行性疾病具有潛在的保護作用。一些研究發現維生素C對認知有重要性，其保護神經元細胞的益處對抗神經變性。

