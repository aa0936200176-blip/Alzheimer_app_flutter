維生素E是一種植物衍生的脂溶性化合物，可以與細胞膜相互作用，清除自由基，參與產生自由基的連鎖反應。Behl等提示：生育酚（維生素E）和17-雌二醇具有神經保護作用，抗氧化並可以抑制阿茲海默症的進展。一些證據表明生育酚可能通過調節阿爾茨海默病相關酶在神經炎症中起作用，攝取富含較高的維生素E飲食可以延緩帕金森氏症的發展，其他已證明用維生素E治療可能會減慢阿茲海默症的進程。

維生素B
維生素B是必不可少的基本微量營養素，可作為其他維生素所必需的輔酶可在抗氧化－抗炎系統中發揮作用。在所有健康的老年人中，幾乎有50%的人血液中的同型半胱氨酸（homocysteine）水平很高，實驗結果表明血液中高半胱氨酸的濃度會破壞血管內皮，促進血栓形成並增加低密度脂蛋白膽固醇，這些因素導致動脈粥狀硬化和心血管疾病的發展，研究也發現高膽固醇對腦細胞有害，患有血管性癡呆和阿茲海默氏病的患者經常具有高同型半胱氨酸。幾項研究報告，葉酸（維生素B9），維生素B6和維生素B12降低了同型半胱氨酸水平，因此，可降低腦循環問題的風險。

維生素D
維生素D接受體已經在大腦的幾個關鍵區域被發現。大腦中存在一些酶可以刺激局部合成骨化三醇（維生素D3的活性形式）。由於其多種作用機制，維生素D可能對血管和神經細胞具有消炎和保護作用，對抗癡呆症的發展。

維生素D缺乏與虛弱有關。特別是來自全國健康與營養調查表明，維生素D缺乏與男性和老年婦女普遍衰弱有關；血清維生素D濃度<15 ng/mL的人出現虛弱的機率是3.7倍。

維生素D與衰弱之間的聯繫可以包括與肌肉力量和骨骼健康有關的生物途徑。低濃度25-羥基維生素D〔25（OH）D〕對鈣的吸收有負面影響，導致髖部骨折增加。另外，骨骼肌細胞受體的25-羥基維生素D（1,25-二羥基維生素D）的形式有助於蛋白質合成，因此，維生素D含量降低可能導致降低肌肉力量。

